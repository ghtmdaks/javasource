package com.hot.game.file;

public class GameFileDTO{

	private Integer gf_num;
	private Integer g_num;
	private String fname;
	private String oname;
	private String kind;

	public Integer getGf_num() {
		return gf_num;
	}

	public void setGf_num(Integer gf_num) {
		this.gf_num = gf_num;
	}

	public Integer getG_num() {
		return g_num;
	}

	public void setG_num(Integer g_num) {
		this.g_num = g_num;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getOname() {
		return oname;
	}

	public void setOname(String oname) {
		this.oname = oname;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

}
