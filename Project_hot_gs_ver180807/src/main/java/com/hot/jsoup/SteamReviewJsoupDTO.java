package com.hot.jsoup;

public class SteamReviewJsoupDTO {

	private String recentReviews;
	private String allReviews;
	private String recentTooltip;
	private String allTooltip;

	public String getRecentReviews() {
		return recentReviews;
	}

	public void setRecentReviews(String recentReviews) {
		this.recentReviews = recentReviews;
	}

	public String getAllReviews() {
		return allReviews;
	}

	public void setAllReviews(String allReviews) {
		this.allReviews = allReviews;
	}

	public String getRecentTooltip() {
		return recentTooltip;
	}

	public void setRecentTooltip(String recentTooltip) {
		this.recentTooltip = recentTooltip;
	}

	public String getAllTooltip() {
		return allTooltip;
	}

	public void setAllTooltip(String allTooltip) {
		this.allTooltip = allTooltip;
	}

}
